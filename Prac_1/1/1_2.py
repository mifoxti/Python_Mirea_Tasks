# Да, в Python есть ограничения на диапазон чисел, как для целых, так и для чисел с плавающей запятой.

# Пример для целых чисел
max_int = 2**63 - 1  # Максимальное значение для 64-битных целых чисел
print("Максимальное значение для целых чисел:", max_int)

# Попытка превысить максимальное значение
overflowed_int = max_int + 1
print("Превышение максимального значения:", overflowed_int)

# Пример для чисел с плавающей запятой
max_float = 1.7976931348623157e308
print("Максимальное значение для чисел с плавающей запятой:", max_float)

# Попытка превысить максимальное значение
overflowed_float = max_float * 2
print("Превышение максимального значения:", overflowed_float)
